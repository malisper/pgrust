/// `strVal`-style read of a `Node::String`'s `sval`.
pub(crate) fn node_sval<'a>(node: &'a Node<'_>) -> &'a str {
    match node {
        Node::String(s) => s.sval.as_str(),
        _ => panic!("indexcmds: expected a String value node in a name list"),
    }
}

/// A list of `Node::String` (the C `List *` of `String` nodes) rendered as a
/// `NameList` (`&[Option<String>]`) for the namespace helpers.
pub(crate) fn name_list(names: &PgVec<'_, types_nodes::nodes::NodePtr<'_>>) -> Vec<Option<String>> {
    names
        .iter()
        .map(|n| Some(node_sval(n).to_string()))
        .collect()
}

/// The same list rendered as `Vec<String>` for `compatible_oper_opid`.
pub(crate) fn name_list_strings(
    names: &PgVec<'_, types_nodes::nodes::NodePtr<'_>>,
) -> Vec<String> {
    names.iter().map(|n| node_sval(n).to_string()).collect()
}

