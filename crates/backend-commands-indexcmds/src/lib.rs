//! `backend/commands/indexcmds.c` — the CREATE INDEX command driver
//! (PostgreSQL 18.3).
//!
//! This unit grounds the high-value CREATE INDEX command path against the
//! repo's owned node / relcache model:
//!
//!  * [`DefineIndex`] — the command-level driver: validate the statement, open
//!    the table, resolve the access method, run [`ComputeIndexAttrs`], assemble
//!    the [`IndexInfo`] + [`IndexCreateArgs`], call `index_create`, and (for a
//!    non-concurrent, non-partitioned build) `index_create` already builds it.
//!  * [`ComputeIndexAttrs`] — per-column opclass / collation / expression
//!    resolution, exclusion / WITHOUT-OVERLAPS operator handling, included-column
//!    restrictions, `indoption` derivation, and per-column `attoptions`.
//!  * [`ResolveOpClass`] / [`GetOperatorFromCompareType`] / [`CheckPredicate`].
//!  * the autogenerated-name choosers
//!    ([`makeObjectName`] / [`ChooseRelationName`] / [`ChooseIndexName`] /
//!    `ChooseIndexNameAddition` / [`ChooseIndexColumnNames`]).
//!
//! `GetDefaultOpClass` lives in the repo's `pg_opclass` / lsyscache owner (a
//! home divergence already merged); [`ResolveOpClass`] reaches it through
//! `lsyscache::get_default_opclass`.
//!
//! ## Deferred (precise seam-panics, tracked)
//!
//! The CONCURRENTLY tail of `DefineIndex` (multi-transaction build/validate
//! state machine: `index_concurrently_build`, `validate_index`,
//! `WaitForLockers`, `WaitForOlderSnapshots`, snapshot/session-lock dance) and
//! the partitioned-table recursion (`generateClonedIndexStmt`,
//! `IndexSetParentIndex`, `BuildIndexInfo`/`CompareIndexInfo` matching) are NOT
//! built here — their substrate (cross-transaction control, partition-index
//! cloning) is unported. They are precise loud panics, not silent stubs.
//!
//! `ReindexIndex` / `ReindexTable` (the REINDEX drivers) seam-panic into the
//! `reindex_index` / `reindex_relation` seams, which are declared but
//! owner-uninstalled (substrate unported).

