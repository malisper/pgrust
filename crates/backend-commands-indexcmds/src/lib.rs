fn virtual_gencol_error(stmt: &IndexStmt<'_>) -> backend_utils_error::PgError {
    let msg = if stmt.primary {
        "primary keys on virtual generated columns are not supported"
    } else if stmt.isconstraint {
        "unique constraints on virtual generated columns are not supported"
    } else {
        "indexes on virtual generated columns are not supported"
    };
    ereport(ERROR)
        .errcode(types_error::ERRCODE_FEATURE_NOT_SUPPORTED)
        .errmsg(msg)
        .into_error()
}

fn virtual_gencol_error_expr(stmt: &IndexStmt<'_>) -> backend_utils_error::PgError {
    let msg = if stmt.isconstraint {
        "unique constraints on virtual generated columns are not supported"
    } else {
        "indexes on virtual generated columns are not supported"
    };
    ereport(ERROR)
        .errcode(types_error::ERRCODE_FEATURE_NOT_SUPPORTED)
        .errmsg(msg)
        .into_error()
}

/// `bms_union` accumulation helper for the two `pull_varattnos` results, each an
/// `Option<PgBox<Bitmapset>>` (the C `Bitmapset *`, NULL == empty).
fn bms_union_opt<'mcx>(
    mcx: Mcx<'mcx>,
    a: Option<mcx::PgBox<'mcx, types_nodes::Bitmapset<'mcx>>>,
    b: Option<mcx::PgBox<'mcx, types_nodes::Bitmapset<'mcx>>>,
) -> PgResult<Option<mcx::PgBox<'mcx, types_nodes::Bitmapset<'mcx>>>> {
    match (a, b) {
        (None, x) => Ok(x),
        (x, None) => Ok(x),
        (Some(a), Some(b)) => Ok(bms_union(mcx, Some(&a), Some(&b))?),
    }
}

