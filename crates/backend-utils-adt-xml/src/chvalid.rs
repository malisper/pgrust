/// `xmlCharInRange(val, rptr)` (libxml2 chvalid.c) — binary search of the
/// (sorted, inclusive) short-range table.
fn xml_char_in_range(val: u32, ranges: &[(u16, u16)]) -> bool {
    // All five tables used here are short-range only; val >= 0x10000 can never
    // be a member.
    let Ok(val) = u16::try_from(val) else {
        return false;
    };
    // C: binary search on { low <= val <= high }.
    ranges
        .binary_search_by(|&(low, high)| {
            if val < low {
                core::cmp::Ordering::Greater
            } else if val > high {
                core::cmp::Ordering::Less
            } else {
                core::cmp::Ordering::Equal
            }
        })
        .is_ok()
}

/// `xmlIsBaseChar_ch(c)` (`<libxml/chvalid.h>`) — the single-byte (< 0x100)
/// part of XML 1.0 BaseChar.
pub fn xml_is_base_char_ch(c: u32) -> bool {
    debug_assert!(c < 0x100);
    (0x41..=0x5a).contains(&c)
        || (0x61..=0x7a).contains(&c)
        || (0xc0..=0xd6).contains(&c)
        || (0xd8..=0xf6).contains(&c)
        || (0xf8 <= c)
}

/// `xmlIsBaseCharQ(c)` (`<libxml/chvalid.h>`).
pub fn xml_is_base_char_q(c: u32) -> bool {
    if c < 0x100 {
        xml_is_base_char_ch(c)
    } else {
        xml_char_in_range(c, XML_IS_BASE_CHAR_SRNG)
    }
}

/// `xmlIsIdeographicQ(c)` (`<libxml/chvalid.h>`) — XML 1.0 Appendix B
/// production [86] Ideographic (inlined ranges, no table).
pub fn xml_is_ideographic_q(c: u32) -> bool {
    if c < 0x100 {
        false
    } else {
        (0x4e00..=0x9fa5).contains(&c) || c == 0x3007 || (0x3021..=0x3029).contains(&c)
    }
}

/// `xmlIsDigit_ch(c)` (`<libxml/chvalid.h>`).
pub fn xml_is_digit_ch(c: u32) -> bool {
    debug_assert!(c < 0x100);
    (0x30..=0x39).contains(&c)
}

/// `xmlIsDigitQ(c)` (`<libxml/chvalid.h>`).
pub fn xml_is_digit_q(c: u32) -> bool {
    if c < 0x100 {
        xml_is_digit_ch(c)
    } else {
        xml_char_in_range(c, XML_IS_DIGIT_SRNG)
    }
}

/// `xmlIsCombiningQ(c)` (`<libxml/chvalid.h>`) — no members below 0x100.
pub fn xml_is_combining_q(c: u32) -> bool {
    if c < 0x100 {
        false
    } else {
        xml_char_in_range(c, XML_IS_COMBINING_SRNG)
    }
}

/// `xmlIsExtender_ch(c)` (`<libxml/chvalid.h>`).
pub fn xml_is_extender_ch(c: u32) -> bool {
    debug_assert!(c < 0x100);
    c == 0xb7
}

/// `xmlIsExtenderQ(c)` (`<libxml/chvalid.h>`).
pub fn xml_is_extender_q(c: u32) -> bool {
    if c < 0x100 {
        xml_is_extender_ch(c)
    } else {
        xml_char_in_range(c, XML_IS_EXTENDER_SRNG)
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    /// Spot checks against known codepoints for every class, including range
    /// boundaries and the gaps between adjacent ranges.
    #[test]
    fn base_char_spot_checks() {
        // ASCII letters (the _ch arm).
        assert!(xml_is_base_char_q('A' as u32));
        assert!(xml_is_base_char_q('z' as u32));
        assert!(!xml_is_base_char_q('0' as u32));
        assert!(!xml_is_base_char_q('_' as u32));
        // Latin-1 letters and the gaps at 0xD7 (×) / 0xF7 (÷).
        assert!(xml_is_base_char_q(0xc0)); // À
        assert!(!xml_is_base_char_q(0xd7)); // ×
        assert!(!xml_is_base_char_q(0xf7)); // ÷
        assert!(xml_is_base_char_q(0xff)); // ÿ
        // Table arm: boundaries of the first and last ranges.
        assert!(xml_is_base_char_q(0x100));
        assert!(xml_is_base_char_q(0x131));
        assert!(!xml_is_base_char_q(0x132)); // gap before 0x134
        assert!(xml_is_base_char_q(0x386)); // singleton Greek ά
        assert!(!xml_is_base_char_q(0x387)); // · is Extender, not BaseChar
        assert!(xml_is_base_char_q(0x3b1)); // α
        assert!(xml_is_base_char_q(0x430)); // Cyrillic а
        assert!(xml_is_base_char_q(0xac00)); // first Hangul syllable
        assert!(xml_is_base_char_q(0xd7a3)); // last Hangul syllable
        assert!(!xml_is_base_char_q(0xd7a4));
        assert!(!xml_is_base_char_q(0x4e2d)); // 中 is Ideographic, not BaseChar
        assert!(!xml_is_base_char_q(0x10000)); // beyond all short ranges
    }

    #[test]
    fn ideographic_spot_checks() {
        assert!(xml_is_ideographic_q(0x4e00));
        assert!(xml_is_ideographic_q(0x4e2d)); // 中
        assert!(xml_is_ideographic_q(0x9fa5));
        assert!(!xml_is_ideographic_q(0x9fa6));
        assert!(xml_is_ideographic_q(0x3007)); // 〇
        assert!(xml_is_ideographic_q(0x3021));
        assert!(xml_is_ideographic_q(0x3029));
        assert!(!xml_is_ideographic_q(0x302a));
        assert!(!xml_is_ideographic_q('A' as u32));
    }

    #[test]
    fn digit_spot_checks() {
        assert!(xml_is_digit_q('0' as u32));
        assert!(xml_is_digit_q('9' as u32));
        assert!(!xml_is_digit_q('a' as u32));
        assert!(xml_is_digit_q(0x660)); // ARABIC-INDIC DIGIT ZERO
        assert!(xml_is_digit_q(0x669));
        assert!(!xml_is_digit_q(0x66a));
        assert!(xml_is_digit_q(0x966)); // DEVANAGARI DIGIT ZERO
        assert!(xml_is_digit_q(0xf29)); // TIBETAN DIGIT NINE (last entry)
        assert!(!xml_is_digit_q(0xf2a));
    }

    #[test]
    fn combining_spot_checks() {
        assert!(xml_is_combining_q(0x300)); // COMBINING GRAVE ACCENT
        assert!(xml_is_combining_q(0x345));
        assert!(!xml_is_combining_q(0x346)); // gap (XML 1.0 stops at 0x345)
        assert!(xml_is_combining_q(0x5bf)); // HEBREW POINT RAFE
        assert!(!xml_is_combining_q(0x5c0));
        assert!(xml_is_combining_q(0x309a)); // last entry
        assert!(!xml_is_combining_q(0x309b));
        assert!(!xml_is_combining_q(0xb7)); // < 0x100 arm is always false
    }

    #[test]
    fn extender_spot_checks() {
        assert!(xml_is_extender_q(0xb7)); // MIDDLE DOT (the _ch arm)
        assert!(!xml_is_extender_q(0xb6));
        assert!(xml_is_extender_q(0x2d0));
        assert!(xml_is_extender_q(0x387)); // GREEK ANO TELEIA
        assert!(xml_is_extender_q(0x3005)); // 々
        assert!(xml_is_extender_q(0x30fc)); // KATAKANA PROLONGED SOUND MARK
        assert!(xml_is_extender_q(0x30fe));
        assert!(!xml_is_extender_q(0x30ff));
    }
}
