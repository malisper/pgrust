//! libxml2 character-range validation — the XML 1.0 (Appendix B) character
//! classes `xml.c` consults for XML-name validity.
//!
//! `xml.c`'s `is_valid_xml_namefirst` / `is_valid_xml_namechar` (xml.c:2353 /
//! 2362) and the `PG_XMLISNAMECHAR` macro (xml.c:1366) are built from libxml2's
//! `chvalid` predicates (`xmlIsBaseCharQ`, `xmlIsIdeographicQ`, `xmlIsDigitQ`,
//! `xmlIsCombiningQ`, `xmlIsExtenderQ` and their single-byte `_ch` fast
//! forms). Those predicates are FIXED static range tables: this module
//! transcribes them exactly from libxml2 `chvalid.c` (generated from
//! `chvalid.def` by `genChRanges.py`; the tables below are byte-identical to
//! libxml2 v2.9.14 `chvalid.c`, which in turn transcribes the XML 1.0 spec's
//! Appendix B productions [85] BaseChar, [86] Ideographic, [87] CombiningChar,
//! [88] Digit, [89] Extender) plus the `< 0x100` macro arms of
//! `<libxml/chvalid.h>`.
//!
//! Each `(low, high)` pair is an INCLUSIVE range (libxml2 `xmlChSRange`). All
//! five groups used here contain only short (`u16`) ranges — none of these XML
//! 1.0 classes has a codepoint above 0xFFFF — so the libxml2 long-range arm of
//! `xmlCharInRange` has no analogue.

