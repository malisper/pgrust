//! Canonical built-in function table — the completeness oracle for the fmgr
//! built-in REGISTRY (`fmgr_builtins[]`).
//!
//! In C, `src/backend/utils/Gen_fmgrtab.pl` reads `pg_proc.dat` at build time
//! and emits `fmgr_builtins[]` — a single static array of EVERY built-in, so
//! `fmgr_isbuiltin(oid)` can never silently miss a real built-in. The port
//! replaced that one table with a per-crate runtime registry (each `adt/*`
//! crate registers its own built-ins in `init_seams()`). That trades the
//! compile-time completeness guarantee for a runtime-assembled set: if a crate
//! is unported or its `init_seams()` is unwired, its built-ins are silently
//! absent and `fmgr_isbuiltin` misses (for catalog-scan comparators this
//! recurses to a stack overflow at boot).
//!
//! This module restores the guarantee as an explicit oracle. [`CANONICAL`] is
//! the exact set `Gen_fmgrtab.pl` would emit for PostgreSQL 18.3: every
//! `pg_proc.dat` row with `prolang == internal` and `prokind != 'a'`
//! (non-aggregate internal-language functions), in OID order, carrying the same
//! `(oid, prosrc, nargs, strict, retset)` `Gen_fmgrtab.pl` writes into each
//! `FmgrBuiltin`. It is the SAME source of truth (`pg_proc.dat`) C uses — not a
//! parallel hand-maintained list.
//!
//! Regeneration (when bumping the vendored PostgreSQL): run, from
//! `postgres-18.3/src/backend/utils`:
//! ```text
//! perl -I ../catalog -e '
//!   use Catalog;
//!   my $h = Catalog::ParseHeader("../../include/catalog/pg_proc.h");
//!   my $rows = Catalog::ParseData("../../include/catalog/pg_proc.dat", $h->{columns}, 0);
//!   my %b = (t=>"true", f=>"false");
//!   for my $r (sort { $a->{oid} <=> $b->{oid} } @$rows) {
//!     next if ($r->{prolang}//"internal") ne "internal";
//!     next if ($r->{prokind}//"f") eq "a";
//!     printf "    (%d, \"%s\", %d, %s, %s),\n", $r->{oid}, $r->{prosrc},
//!       $r->{pronargs}, $b{$r->{proisstrict}//"t"}, $b{$r->{proretset}//"f"};
//!   }'
//! ```
//! This file is GENERATED; do not hand-edit the table body.

