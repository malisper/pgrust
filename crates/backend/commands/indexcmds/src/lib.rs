/// `stmt->relation && !stmt->relation->inh` (the recursion-declined-but-
/// partitions-exist invalidation predicate).
fn relation_inh_declined(stmt: &IndexStmt<'_>) -> bool {
    match index_stmt_relation(stmt) {
        Some(rv) => !rv.inh,
        None => false,
    }
}

/// `!stmt->relation || stmt->relation->inh` (recurse-into-partitions predicate).
fn relation_should_recurse(stmt: &IndexStmt<'_>) -> bool {
    match index_stmt_relation(stmt) {
        Some(rv) => rv.inh,
        None => true,
    }
}

