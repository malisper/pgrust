/// Install this unit's outward seams.
pub fn init_seams() {
    // makeObjectName(name1, name2, label) — name2/label are non-null at the
    // pg_constraint call sites (ChooseConstraintName), matching the &str seam.
    indexcmds_seams::make_object_name::set(|name1, name2, label| {
        // The seam flattens C's `const char *name2`/`const char *label` (both of
        // which may be NULL) to `&str`; an empty value is the NULL case, so the
        // corresponding separating underscore is omitted. E.g. `makeArrayTypeName`
        // calls `makeObjectName("", typeName, NULL)` for the no-suffix array name,
        // which must yield `_typeName` (not `_typeName_`); callers like
        // `ChooseConstraintName` for a domain constraint pass NULL name2 → empty.
        let name2 = if name2.is_empty() { None } else { Some(name2) };
        let label = if label.is_empty() { None } else { Some(label) };
        choosers::makeObjectName(name1, name2, label)
    });

    // GetDefaultOpClass(type_id, am_id) lives in indexcmds.c; the canonical
    // public surface is lsyscache's get_default_opclass convenience wrapper, so
    // the owner installs that seam here.
    lsyscache_seams::get_default_opclass::set(opclass::GetDefaultOpClass);

    // GetOperatorFromCompareType(opclass, rhstype, cmptype) -> (op, strat).
    indexcmds_seams::get_operator_from_compare_type::set(
        |opclass, rhstype, cmptype| {
            let mut op = InvalidOid;
            let mut strat: u16 = 0;
            opclass::GetOperatorFromCompareType(opclass, rhstype, cmptype, &mut op, &mut strat)?;
            Ok((op, strat))
        },
    );

    // DefineIndex(tableId, stmt, ...) — used by the bootstrap (BKI) grammar's
    // DECLARE [UNIQUE] INDEX actions (with skip_build = true).
    indexcmds_seams::define_index::set(|mcx, args| {
        DefineIndex(
            mcx,
            args.table_id,
            &args.stmt,
            args.index_relation_id,
            args.parent_index_id,
            args.parent_constraint_id,
            args.total_parts,
            args.is_alter_table,
            args.check_rights,
            args.check_not_in_use,
            args.skip_build,
            args.quiet,
        )
    });

    // DefineIndex — lifetime-generic form for ALTER TABLE ATExecAddIndex.
    indexcmds_seams::define_index_full::set(|mcx, args| {
        DefineIndex(
            mcx,
            args.table_id,
            &args.stmt,
            args.index_relation_id,
            args.parent_index_id,
            args.parent_constraint_id,
            args.total_parts,
            args.is_alter_table,
            args.check_rights,
            args.check_not_in_use,
            args.skip_build,
            args.quiet,
        )
    });

    indexcmds_seams::check_index_compatible::set(|mcx, old_id, stmt| {
        check_index_compatible_stmt(mcx, old_id, stmt)
    });

    // --- ProcessUtilitySlow CREATE INDEX arm (utility.c:1455-1560) ---
    // The three CREATE-INDEX dispatch seams the command owner installs.
    utility_out_seams::range_var_get_relid_owns_relation::set(
        utility_range_var_get_relid_owns_relation,
    );
    utility_out_seams::create_index_count_partitions::set(
        create_index_count_partitions,
    );
    utility_out_seams::define_index::set(define_index_dispatch_arm);
    utility_out_seams::exec_reindex::set(exec_reindex_dispatch_arm);
}

