/// `DefineIndex(tableId, stmt, indexRelationId, parentIndexId,
/// parentConstraintId, total_parts, is_alter_table, check_rights,
/// check_not_in_use, skip_build, quiet)`.
///
/// Ported for the non-concurrent, non-partitioned core (the high-value CREATE
/// INDEX command path) plus the CREATE INDEX CONCURRENTLY multi-transaction
/// build/validate tail. The partitioned-table recursion is a precise
/// seam-panic — see the module docs and [`define_index_partition_recursion`].
///
/// Returns the [`ObjectAddress`] of the created index.
pub fn DefineIndex<'mcx>(
    mcx: Mcx<'mcx>,
    table_id: Oid,
    stmt: &IndexStmt<'mcx>,
    mut index_relation_id: Oid,
    parent_index_id: Oid,
    parent_constraint_id: Oid,
    mut total_parts: i32,
    is_alter_table: bool,
    check_rights: bool,
    check_not_in_use: bool,
    skip_build: bool,
    quiet: bool,
) -> PgResult<ObjectAddress> {
    let mut root_save_nestlevel = NewGUCNestLevel();

    restrict_search_path::call()?;

    // Some callers need us to run with an empty default_tablespace; this is a
    // necessary hack to be able to reproduce catalog state accurately when
    // recreating indexes after table-rewriting ALTER TABLE.
    //
    // C: set_config_option("default_tablespace", "", PGC_USERSET, PGC_S_SESSION,
    //    GUC_ACTION_SAVE, true, 0, false). The repo seam is the `SetConfigOption`
    //    macro shape (no explicit GUC_ACTION_SAVE); the action defaults there.
    if stmt.reset_default_tblspc {
        set_config_option::call(
            "default_tablespace",
            "",
            types_guc::guc::GucContext::PGC_USERSET,
            types_guc::guc::GucSource::PGC_S_SESSION,
        )?;
    }

    // Force non-concurrent build on temporary relations.
    let concurrent =
        stmt.concurrent && lsyscache::get_rel_persistence::call(table_id)? != RELPERSISTENCE_TEMP;

    // Start progress report (skipped when building a partition).
    if !OidIsValid(parent_index_id) {
        pgstat_progress_start_command(ProgressCommandType::CreateIndex, table_id);
        pgstat_progress_update_param(
            PROGRESS_CREATEIDX_COMMAND,
            if concurrent {
                PROGRESS_CREATEIDX_COMMAND_CREATE_CONCURRENTLY
            } else {
                PROGRESS_CREATEIDX_COMMAND_CREATE
            },
        );
    }

    // No index OID to report yet.
    pgstat_progress_update_param(PROGRESS_CREATEIDX_INDEX_OID, InvalidOid as i64);

    // count key attributes in index
    let number_of_key_attributes = stmt.indexParams.len() as i32;

    // Build the combined list of (key + INCLUDE) index parameters, as IndexElems.
    let all_index_params: Vec<&IndexElem<'mcx>> = stmt
        .indexParams
        .iter()
        .chain(stmt.indexIncludingParams.iter())
        .map(|n| node_as_index_elem(n.as_ref()))
        .collect();
    let number_of_attributes = all_index_params.len() as i32;

    if number_of_key_attributes <= 0 {
        return Err(ereport(ERROR)
            .errcode(::types_error::ERRCODE_INVALID_OBJECT_DEFINITION)
            .errmsg("must specify at least one column")
            .into_error());
    }
    if number_of_attributes > INDEX_MAX_KEYS {
        return Err(ereport(ERROR)
            .errcode(::types_error::ERRCODE_TOO_MANY_COLUMNS)
            .errmsg(format!(
                "cannot use more than {INDEX_MAX_KEYS} columns in an index"
            ))
            .into_error());
    }

    // Take the table lock. ShareUpdateExclusiveLock for concurrent, ShareLock
    // otherwise.
    let lockmode: ::types_storage::lock::LOCKMODE = if concurrent {
        ::types_storage::lock::ShareUpdateExclusiveLock
    } else {
        ::types_storage::lock::ShareLock
    };
    let rel = table_seam::table_open::call(mcx, table_id, lockmode)?;

    // Switch to the table owner's userid + lock down security-restricted ops.
    let (root_save_userid, root_save_sec_context) = GetUserIdAndSecContext();
    SetUserIdAndSecContext(
        rel.rd_rel.relowner,
        root_save_sec_context | SECURITY_RESTRICTED_OPERATION,
    );

    let namespace_id = rel.rd_rel.relnamespace;

    // Exclusion behavior: EXCLUDE constraint or temporal PRIMARY KEY/UNIQUE.
    let exclusion = !stmt.excludeOpNames.is_empty() || stmt.iswithoutoverlaps;

    // Ensure it makes sense to index this kind of relation.
    match rel.rd_rel.relkind {
        x if x == RELKIND_RELATION || x == RELKIND_MATVIEW || x == RELKIND_PARTITIONED_TABLE => {}
        _ => {
            let name = rel.rd_rel.relname.as_str().to_string();
            let detail = pg_class_seams::errdetail_relkind_not_supported::call(
                rel.rd_rel.relkind,
            )?;
            return Err(ereport(ERROR)
                .errcode(::types_error::ERRCODE_WRONG_OBJECT_TYPE)
                .errmsg(format!("cannot create index on relation \"{name}\""))
                .errdetail(detail)
                .into_error());
        }
    }

    // Partitioned tables: we only create catalog entries; actual indexes are
    // built by recursing per partition.
    let partitioned = rel.rd_rel.relkind == RELKIND_PARTITIONED_TABLE;
    if partitioned && stmt.concurrent {
        let name = rel.rd_rel.relname.as_str().to_string();
        return Err(ereport(ERROR)
            .errcode(::types_error::ERRCODE_FEATURE_NOT_SUPPORTED)
            .errmsg(format!(
                "cannot create index on partitioned table \"{name}\" concurrently"
            ))
            .into_error());
    }

    // Unless the caller vouches for it, insist the table not be in use.
    if check_not_in_use {
        tablecmds_seam::check_table_not_in_use::call(&rel, "CREATE INDEX")?;
    }

    // Verify CREATE rights in the rel's namespace.
    if check_rights {
        let aclresult =
            aclchk_seam::object_aclcheck::call(NAMESPACE_RELATION_ID, namespace_id, root_save_userid, ACL_CREATE)?;
        if aclresult != ACLCHECK_OK {
            aclchk_seam::aclcheck_error::call(
                aclresult,
                OBJECT_SCHEMA,
                Some(namespacename_seam::get_namespace_name::call(namespace_id)?),
            )?;
        }
    }

    // Select tablespace.
    let mut tablespace_id: Oid;
    if let Some(ts) = stmt.tableSpace.as_ref() {
        tablespace_id = get_tablespace_oid(mcx, ts.as_str(), false)?;
        if partitioned && tablespace_id == my_database_tablespace()? {
            return Err(ereport(ERROR)
                .errcode(::types_error::ERRCODE_FEATURE_NOT_SUPPORTED)
                .errmsg("cannot specify default tablespace for partitioned relations")
                .into_error());
        }
    } else {
        tablespace_id = GetDefaultTablespace(mcx, rel.rd_rel.relpersistence as i8, partitioned)?;
    }

    // Check tablespace permissions.
    if check_rights && OidIsValid(tablespace_id) && tablespace_id != my_database_tablespace()? {
        let aclresult = aclchk_seam::object_aclcheck::call(
            TABLESPACE_RELATION_ID,
            tablespace_id,
            root_save_userid,
            ACL_CREATE,
        )?;
        if aclresult != ACLCHECK_OK {
            let tsname = get_tablespace_name(mcx, tablespace_id)?.map(|s| s.as_str().to_string());
            aclchk_seam::aclcheck_error::call(aclresult, OBJECT_TABLESPACE, tsname)?;
        }
    }

    // Force shared indexes into pg_global; reject pg_global for non-shared.
    if rel.rd_rel.relisshared {
        tablespace_id = GLOBALTABLESPACE_OID;
    } else if tablespace_id == GLOBALTABLESPACE_OID {
        return Err(ereport(ERROR)
            .errcode(::types_error::ERRCODE_INVALID_PARAMETER_VALUE)
            .errmsg("only shared relations can be placed in pg_global tablespace")
            .into_error());
    }

    // Choose the index column names.
    let index_col_names = ChooseIndexColumnNames(&all_index_params);

    // Select index name if caller didn't specify.
    let index_relation_name: String = match stmt.idxname.as_ref() {
        Some(n) => n.as_str().to_string(),
        None => ChooseIndexName(
            mcx,
            rel.rd_rel.relname.as_str(),
            namespace_id,
            &index_col_names,
            !stmt.excludeOpNames.is_empty(),
            stmt.primary,
            stmt.isconstraint,
        )?,
    };

    // Look up the access method; "rtree" -> "gist" hack.
    let mut access_method_name: String = stmt
        .accessMethod
        .as_ref()
        .map(|a| a.as_str().to_string())
        .expect("DefineIndex: IndexStmt has no accessMethod");
    let mut am_info = syscache::search_am_by_name::call(mcx, &access_method_name)?;
    if am_info.is_none() && access_method_name == "rtree" {
        ereport(::types_error::NOTICE)
            .errmsg("substituting access method \"gist\" for obsolete method \"rtree\"")
            .finish(here("DefineIndex"))?;
        access_method_name = "gist".to_string();
        am_info = syscache::search_am_by_name::call(mcx, &access_method_name)?;
    }
    let am_info = match am_info {
        Some(a) => a,
        None => {
            return Err(ereport(ERROR)
                .errcode(::types_error::ERRCODE_UNDEFINED_OBJECT)
                .errmsg(format!("access method \"{access_method_name}\" does not exist"))
                .into_error());
        }
    };
    let access_method_id = am_info.oid;
    let am_routine = GetIndexAmRoutineByAmId(access_method_id)?;

    pgstat_progress_update_param(PROGRESS_CREATEIDX_ACCESS_METHOD_OID, access_method_id as i64);

    if stmt.unique && !stmt.iswithoutoverlaps && !am_routine.amcanunique {
        return Err(amerr("does not support unique indexes", &access_method_name));
    }
    if !stmt.indexIncludingParams.is_empty() && !am_routine.amcaninclude {
        return Err(amerr("does not support included columns", &access_method_name));
    }
    if number_of_key_attributes > 1 && !am_routine.amcanmulticol {
        return Err(amerr("does not support multicolumn indexes", &access_method_name));
    }
    if exclusion && am_routine.amgettuple.is_none() {
        return Err(amerr("does not support exclusion constraints", &access_method_name));
    }
    if stmt.iswithoutoverlaps && access_method_name != "gist" {
        return Err(amerr("does not support WITHOUT OVERLAPS constraints", &access_method_name));
    }

    let amcanorder = am_routine.amcanorder;
    let amissummarizing = am_routine.amsummarizing;

    // Validate predicate, if given.
    if let Some(where_clause) = stmt.whereClause.as_ref() {
        let pred_expr = where_clause
            .as_ref()
            .as_expr()
            .expect("DefineIndex: whereClause is not an Expr")
            .clone();
        CheckPredicate(mcx, pred_expr)?;
    }

    // Parse AM-specific options, convert to text-array form, validate.
    //
    // C: reloptions = transformRelOptions((Datum) 0, stmt->options, NULL, NULL,
    //    false, false); (void) index_reloptions(amoptions, reloptions, true);
    //    where amoptions = accessMethodForm->amoptions.
    //
    // `transformRelOptionsBytes` builds the on-disk `text[]` varlena image (the
    // bytes `pg_class.reloptions` stores); it rides the `::types_tuple::Datum`
    // by-reference lane as `Datum::ByRef` so `index_create`'s catalog write
    // (`reloptions_to_bytes`) reads the real array (the Datum-bridge fix —
    // construct_text_array_bytes, mirroring the array-producer ByRef fixes).
    let reloptions: ::types_tuple::Datum<'mcx> = if stmt.options.is_empty() {
        ::types_tuple::Datum::null()
    } else {
        // amoptions = accessMethodForm->amoptions; the port keys the AM's option
        // parser on the handler OID (rd_amhandler), the same dispatch the
        // relcache uses for index_reloptions.
        let amhandler = match syscache::search_am_handler::call(access_method_id)? {
            Some(h) => h,
            None => InvalidOid,
        };
        let bytes = transform_index_reloptions_byref(mcx, &stmt.options, amhandler)?;
        ::types_tuple::Datum::from_byref_bytes_in(mcx, &bytes)?
    };

    // Prepare the IndexInfo. Predicates must be in implicit-AND format. In a
    // concurrent build, mark it not-ready-for-inserts.
    let predicate_list: Vec<Expr> = match stmt.whereClause.as_ref() {
        Some(wc) => make_ands_implicit(
            wc.as_ref().as_expr().map(|e| e.clone()),
        ),
        None => Vec::new(),
    };

    let mut index_info = make_index_info(
        number_of_attributes,
        number_of_key_attributes,
        access_method_id,
        (),
        (),
        stmt.unique,
        stmt.nulls_not_distinct,
        !concurrent,
        concurrent,
        amissummarizing,
        stmt.iswithoutoverlaps,
    );
    index_info.ii_Context = Some(mcx);
    if !predicate_list.is_empty() {
        let mut pv = PgVec::new_in(mcx);
        for e in predicate_list {
            pv.push(e);
        }
        index_info.ii_Predicate = Some(pv);
    }

    let mut type_ids = vec_oid(number_of_attributes);
    let mut collation_ids = vec_oid(number_of_attributes);
    let mut opclass_ids = vec_oid(number_of_attributes);
    let mut opclass_options: Vec<::types_tuple::Datum<'mcx>> =
        (0..number_of_attributes).map(|_| ::types_tuple::Datum::null()).collect();
    let mut col_options = vec![0i16; number_of_attributes as usize];

    ComputeIndexAttrs(
        mcx,
        &mut index_info,
        &rel,
        &mut type_ids,
        &mut collation_ids,
        &mut opclass_ids,
        &mut opclass_options,
        &mut col_options,
        &all_index_params,
        if stmt.excludeOpNames.is_empty() {
            None
        } else {
            Some(&stmt.excludeOpNames)
        },
        table_id,
        &access_method_name,
        access_method_id,
        amcanorder,
        stmt.isconstraint,
        stmt.iswithoutoverlaps,
        root_save_userid,
        root_save_sec_context,
        &mut root_save_nestlevel,
    )?;

    // Extra checks when creating a PRIMARY KEY index.
    if stmt.primary {
        index_check_primary_key(mcx, &rel, &index_info, is_alter_table)?;
    }

    // Partitioned + (unique | exclusion): partition-key subset check.
    //
    // If this table is partitioned and we're creating a unique index, primary
    // key, or exclusion constraint, make sure that the partition key is a
    // subset of the index's columns.  Otherwise it would be possible to violate
    // uniqueness by putting values that ought to be unique in different
    // partitions.
    if partitioned && (stmt.unique || exclusion) {
        define_index_partition_keycheck(
            mcx,
            &rel,
            stmt,
            exclusion,
            &index_info,
            &collation_ids,
            &opclass_ids,
        )?;
    }

    // Disallow indexes on system columns + virtual generated columns.
    for i in 0..index_info.ii_NumIndexAttrs as usize {
        let attno = index_info.ii_IndexAttrNumbers[i];
        if attno < 0 {
            return Err(ereport(ERROR)
                .errcode(::types_error::ERRCODE_FEATURE_NOT_SUPPORTED)
                .errmsg("index creation on system columns is not supported")
                .into_error());
        }
        if attno > 0
            && rel.rd_att.attr((attno - 1) as usize).attgenerated == ATTRIBUTE_GENERATED_VIRTUAL
        {
            return Err(virtual_gencol_error(stmt));
        }
    }

    // Check for system and generated columns used in expressions / predicates.
    if index_info.ii_Expressions.is_some() || index_info.ii_Predicate.is_some() {
        let mut indexattrs: Option<::mcx::PgBox<'mcx, ::nodes::Bitmapset<'mcx>>> = None;
        if let Some(exprs) = index_info.ii_Expressions.as_ref() {
            for e in exprs.iter() {
                indexattrs = bms_union_opt(mcx, indexattrs, var_seam::pull_varattnos::call(mcx, e, 1)?)?;
            }
        }
        if let Some(preds) = index_info.ii_Predicate.as_ref() {
            for e in preds.iter() {
                indexattrs = bms_union_opt(mcx, indexattrs, var_seam::pull_varattnos::call(mcx, e, 1)?)?;
            }
        }

        let attrs_ref = indexattrs.as_deref();
        let mut i = FirstLowInvalidHeapAttributeNumber as i32 + 1;
        while i < 0 {
            if bms_is_member(i - FirstLowInvalidHeapAttributeNumber as i32, attrs_ref) {
                return Err(ereport(ERROR)
                    .errcode(::types_error::ERRCODE_FEATURE_NOT_SUPPORTED)
                    .errmsg("index creation on system columns is not supported")
                    .into_error());
            }
            i += 1;
        }

        let mut j = -1;
        loop {
            j = bms_next_member(attrs_ref, j);
            if j < 0 {
                break;
            }
            let attno = j + FirstLowInvalidHeapAttributeNumber as i32;
            if attno > 0
                && rel.rd_att.attr((attno - 1) as usize).attgenerated == ATTRIBUTE_GENERATED_VIRTUAL
            {
                return Err(virtual_gencol_error_expr(stmt));
            }
        }
    }

    // Is the index safe for others to ignore? (only relevant to concurrent.)
    let safe_index = index_info.ii_Expressions.is_none() && index_info.ii_Predicate.is_none();
    let _ = safe_index;

    // Report index creation (constraint case), as a DEBUG message — omitted
    // (DEBUG1 chatter; not observable behavior).
    let _ = quiet;

    // Make the catalog entries (and, unless skipped/concurrent/partitioned,
    // build the index).
    let mut flags: u16 = 0;
    let mut constr_flags: u16 = 0;
    if stmt.isconstraint {
        flags |= INDEX_CREATE_ADD_CONSTRAINT;
    }
    if skip_build || concurrent || partitioned {
        flags |= INDEX_CREATE_SKIP_BUILD;
    }
    if stmt.if_not_exists {
        flags |= INDEX_CREATE_IF_NOT_EXISTS;
    }
    if concurrent {
        flags |= INDEX_CREATE_CONCURRENT;
    }
    if partitioned {
        flags |= INDEX_CREATE_PARTITIONED;
    }
    if stmt.primary {
        flags |= INDEX_CREATE_IS_PRIMARY;
    }

    // If partitioned, recursion declined but partitions exist -> mark invalid.
    if partitioned && relation_inh_declined(stmt) && relation_has_partitions(mcx, &rel)? {
        flags |= INDEX_CREATE_INVALID;
    }

    if stmt.deferrable {
        constr_flags |= INDEX_CONSTR_CREATE_DEFERRABLE;
    }
    if stmt.initdeferred {
        constr_flags |= INDEX_CONSTR_CREATE_INIT_DEFERRED;
    }
    if stmt.iswithoutoverlaps {
        constr_flags |= INDEX_CONSTR_CREATE_WITHOUT_OVERLAPS;
    }

    // Assemble IndexCreateArgs and call index_create. The per-column
    // opclassOptions cross as Some(vec) (one Datum per attribute).
    let col_names_owned: Vec<String> = index_col_names.clone();
    let collation_ids_v = collation_ids.clone();
    let opclass_ids_v = opclass_ids.clone();
    let coloptions_v = col_options.clone();

    let args = IndexCreateArgs {
        index_relation_name: index_relation_name.clone(),
        index_relation_id,
        parent_index_relid: parent_index_id,
        parent_constraint_id,
        rel_file_number: stmt.oldNumber,
        index_info,
        index_col_names: col_names_owned,
        access_method_id,
        table_space_id: tablespace_id,
        collation_ids: collation_ids_v,
        opclass_ids: opclass_ids_v,
        coloptions: coloptions_v,
        reloptions,
        opclass_options: Some(opclass_options),
        stattargets: None, // C: stattargets == NULL
        flags,
        constr_flags,
        allow_system_table_mods: allow_system_table_mods(),
        is_internal: !check_rights,
    };

    let (created_index_relation_id, created_constraint_id) = index_seam::index_create::call(&rel, args)?;
    index_relation_id = created_index_relation_id;

    let address = ObjectAddress {
        classId: RELATION_RELATION_ID,
        objectId: index_relation_id,
        objectSubId: 0,
    };

    if !OidIsValid(index_relation_id) {
        // IF NOT EXISTS hit an existing index: roll back GUC + userid, close.
        at_eoxact_guc(false, root_save_nestlevel);
        SetUserIdAndSecContext(root_save_userid, root_save_sec_context);
        owner_table_close(rel, ::types_storage::lock::NoLock)?;
        if !OidIsValid(parent_index_id) {
            pgstat_progress_end_command();
        }
        return Ok(address);
    }

    // Roll back GUC changes from index functions; keep subsequent changes local.
    at_eoxact_guc(false, root_save_nestlevel);
    root_save_nestlevel = NewGUCNestLevel();
    restrict_search_path::call()?;

    // Add any requested comment.
    if let Some(comment) = stmt.idxcomment.as_ref() {
        CreateComments(mcx, index_relation_id, RELATION_RELATION_ID, 0, Some(comment.as_str()))?;
    }

    // Partitioned-table recursion: build/attach child indexes (indexcmds.c
    // DefineIndex partitioned branch, lines ~1292-1600).
    if partitioned {
        let partdesc = partdesc::RelationGetPartitionDesc(mcx, &rel, true)?;

        // Unless caller specified to skip this step (via ONLY), process each
        // partition to make sure they all contain a corresponding index. If we're
        // called internally (no stmt->relation), recurse always.
        if (stmt.relation.is_none() || relation_should_recurse(stmt)) && partdesc.nparts > 0 {
            // Make a local copy of partdesc->oids[], just for safety.
            let part_oids: Vec<Oid> = partdesc.oids.iter().copied().collect();
            let mut invalidate_parent = false;

            // Report the total number of partitions at the start of the command;
            // don't update it when being called recursively.
            if !OidIsValid(parent_index_id) {
                if total_parts < 0 {
                    let children = inherits_seam::find_all_inheritors::call(
                        mcx,
                        table_id,
                        ::types_storage::lock::NoLock,
                    )?;
                    total_parts = children.len() as i32 - 1;
                }
                pgstat_progress_update_param(
                    PROGRESS_CREATEIDX_PARTITIONS_TOTAL,
                    total_parts as i64,
                );
            }

            // We'll need an IndexInfo describing the parent index, built the same
            // way (BuildIndexInfo) so it will match those for child indexes.
            let parent_index = indexam_seam::index_open::call(mcx, index_relation_id, lockmode)?;
            let parent_index_info = ::index::BuildIndexInfo(mcx, &parent_index)?;
            let parent_index_collations: Vec<Oid> = parent_index.rd_indcollation.to_vec();
            let parent_index_opfamilies: Vec<Oid> = parent_index.rd_opfamily.to_vec();

            // For each partition, scan all existing indexes; if one matches our
            // index definition and is not already attached to some other parent
            // index, attach it. If none matches, build a new index by recursing.
            for &child_relid in part_oids.iter() {
                let childrel = table_seam::table_open::call(mcx, child_relid, lockmode)?;

                let (child_save_userid, child_save_sec_context) = GetUserIdAndSecContext();
                SetUserIdAndSecContext(
                    childrel.rd_rel.relowner,
                    child_save_sec_context | SECURITY_RESTRICTED_OPERATION,
                );
                let child_save_nestlevel = NewGUCNestLevel();
                restrict_search_path::call()?;

                // Don't try to create indexes on foreign tables.
                if childrel.rd_rel.relkind == RELKIND_FOREIGN_TABLE {
                    if stmt.unique || stmt.primary {
                        return Err(ereport(ERROR)
                            .errcode(::types_error::ERRCODE_WRONG_OBJECT_TYPE)
                            .errmsg(format!(
                                "cannot create unique index on partitioned table \"{}\"",
                                rel.rd_rel.relname.as_str()
                            ))
                            .errdetail(format!(
                                "Table \"{}\" contains partitions that are foreign tables.",
                                rel.rd_rel.relname.as_str()
                            ))
                            .into_error());
                    }
                    at_eoxact_guc(false, child_save_nestlevel);
                    SetUserIdAndSecContext(child_save_userid, child_save_sec_context);
                    owner_table_close(childrel, lockmode)?;
                    continue;
                }

                let childidxs =
                    relcache_seam::relation_get_index_list::call(mcx, &childrel)?;
                let attmap = next::attmap::build_attrmap_by_name(
                    mcx,
                    &childrel.rd_att,
                    &rel.rd_att,
                    false,
                )?;

                let mut found = false;
                for &cldidxid in childidxs.iter() {
                    // this index is already partition of another one
                    if inherits_seam::has_superclass::call(cldidxid)? {
                        continue;
                    }

                    let cldidx = indexam_seam::index_open::call(mcx, cldidxid, lockmode)?;
                    let cld_idx_info = ::index::BuildIndexInfo(mcx, &cldidx)?;
                    if ::index::CompareIndexInfo(
                        mcx,
                        &cld_idx_info,
                        &parent_index_info,
                        &cldidx.rd_indcollation,
                        &parent_index_collations,
                        &cldidx.rd_opfamily,
                        &parent_index_opfamilies,
                        &attmap.attnums,
                    )? {
                        let mut cld_constr_oid = InvalidOid;

                        // If this index is being created in the parent because of
                        // a constraint, the child needs a constraint also; if none,
                        // this index is no good, so keep looking.
                        if OidIsValid(created_constraint_id) {
                            cld_constr_oid =
                                pg_constraint::get_relation_idx_constraint_oid(
                                    child_relid,
                                    cldidxid,
                                )?;
                            if !OidIsValid(cld_constr_oid) {
                                cldidx.close(lockmode)?;
                                continue;
                            }
                        }

                        // Attach index to parent and we're done.
                        IndexSetParentIndex(mcx, &cldidx, index_relation_id)?;
                        if OidIsValid(created_constraint_id) {
                            pg_constraint::ConstraintSetParentConstraint(
                                mcx,
                                cld_constr_oid,
                                created_constraint_id,
                                child_relid,
                            )?;
                        }

                        if !cldidx
                            .rd_index
                            .as_ref()
                            .map(|i| i.indisvalid)
                            .unwrap_or(false)
                        {
                            invalidate_parent = true;
                        }

                        found = true;

                        pgstat_progress_incr_param(PROGRESS_CREATEIDX_PARTITIONS_DONE, 1);

                        // keep lock till commit
                        cldidx.close(::types_storage::lock::NoLock)?;
                        break;
                    }

                    cldidx.close(lockmode)?;
                }

                at_eoxact_guc(false, child_save_nestlevel);
                SetUserIdAndSecContext(child_save_userid, child_save_sec_context);
                owner_table_close(childrel, ::types_storage::lock::NoLock)?;

                // If no matching index was found, create our own.
                if !found {
                    // Build an IndexStmt describing the desired child index as in
                    // ATTACH PARTITION (search-path-independent via the cloner).
                    let (child_stmt, _child_constr_oid) =
                        utilcmd_seam::generateClonedIndexStmt::call(
                            mcx,
                            None,
                            &parent_index,
                            &attmap,
                        )?;

                    // Recurse as the starting user ID. Callee will use that for
                    // permission checks, then switch again.
                    SetUserIdAndSecContext(root_save_userid, root_save_sec_context);
                    let child_addr = DefineIndex(
                        mcx,
                        child_relid,
                        &child_stmt,
                        InvalidOid,        // no predefined OID
                        index_relation_id, // this is our child
                        created_constraint_id,
                        -1,
                        is_alter_table,
                        check_rights,
                        check_not_in_use,
                        skip_build,
                        quiet,
                    )?;
                    SetUserIdAndSecContext(child_save_userid, child_save_sec_context);

                    // The index just created could have been switched to invalid
                    // when recursing across multiple partition levels.
                    if !lsyscache::get_index_isvalid::call(child_addr.objectId)? {
                        invalidate_parent = true;
                    }
                }
            }

            parent_index.close(lockmode)?;

            // The pg_index row we inserted was marked indisvalid=true. But if we
            // attached an existing index that is invalid, update our row to
            // invalid too.
            if invalidate_parent {
                index_seam::index_mark_invalid::call(index_relation_id)?;
                // CCI here to make this update visible, in case this recurses
                // across multiple partition levels.
                xact_seam::command_counter_increment::call()?;
            }
        }

        // Indexes on partitioned tables are not themselves built, so we're done.
        at_eoxact_guc(false, root_save_nestlevel);
        SetUserIdAndSecContext(root_save_userid, root_save_sec_context);
        owner_table_close(rel, ::types_storage::lock::NoLock)?;
        if !OidIsValid(parent_index_id) {
            pgstat_progress_end_command();
        } else {
            pgstat_progress_incr_param(PROGRESS_CREATEIDX_PARTITIONS_DONE, 1);
        }
        return Ok(address);
    }

    at_eoxact_guc(false, root_save_nestlevel);
    SetUserIdAndSecContext(root_save_userid, root_save_sec_context);

    if !concurrent {
        // Non-concurrent: index_create already built it; close and we're done.
        owner_table_close(rel, ::types_storage::lock::NoLock)?;
        if !OidIsValid(parent_index_id) {
            pgstat_progress_end_command();
        } else {
            pgstat_progress_incr_param(PROGRESS_CREATEIDX_PARTITIONS_DONE, 1);
        }
        return Ok(address);
    }

    // ---------------------------------------------------------------------
    // CONCURRENTLY build/validate state machine (indexcmds.c:1619-1829).
    // ---------------------------------------------------------------------

    // Save lockrelid and locktag for below, then close rel (keep the lock).
    let heaprelid = relcache_seam::rel_lock_relid::call(rel.rd_id)?;
    let heaplocktag = lmgr_seam::set_locktag_relation::call(heaprelid.dbId, heaprelid.relId);
    owner_table_close(rel, ::types_storage::lock::NoLock)?;

    // For a concurrent build, we must make the catalog entries visible to other
    // transactions before we start to build the index (the new index is marked
    // not indisready / not indisvalid, so no one inserts into it or uses it for
    // queries). Commit our current transaction so the index becomes visible;
    // then start another. Before committing, take a session-level lock on the
    // table so neither it nor the index can be dropped before we finish (cannot
    // block — we already hold the same lock within our transaction).
    lmgr_seam::lock_relation_id_for_session::call(heaprelid, ShareUpdateExclusiveLock_v())?;

    snapmgr_seam::pop_active_snapshot::call()?;
    xact_seam::commit_transaction_command::call()?;
    xact_seam::start_transaction_command::call()?;

    // Tell concurrent index builds to ignore us, if index qualifies.
    if safe_index {
        proc_seam::set_indexsafe_procflags::call()?;
    }

    // The index is now visible, so we can report the OID; include the report for
    // the beginning of phase 2.
    pgstat_progress_update_param(PROGRESS_CREATEIDX_INDEX_OID as i32, index_relation_id as i64);
    pgstat_progress_update_param(PROGRESS_CREATEIDX_PHASE, PROGRESS_CREATEIDX_PHASE_WAIT_1);

    // Phase 2: wait until no running transaction could have the table open with
    // the old list of indexes. ShareLock considers transactions holding locks
    // that permit writing to the table. (Real lock acquisition, not a sleep, so
    // deadlocks are detected.)
    lmgr_seam::wait_for_lockers::call(heaplocktag, ShareLock_v(), true)?;

    // Set ActiveSnapshot since functions in the indexes may need it.
    snapmgr_seam::push_active_snapshot::call(alloc::rc::Rc::new(
        snapmgr_seam::get_transaction_snapshot::call()?,
    ))?;

    // Perform concurrent build of the index (first pass).
    ::index::index_concurrently_build(mcx, table_id, index_relation_id)?;

    // We can do away with our snapshot.
    snapmgr_seam::pop_active_snapshot::call()?;

    // Commit this transaction to make the indisready update visible.
    xact_seam::commit_transaction_command::call()?;
    xact_seam::start_transaction_command::call()?;

    if safe_index {
        proc_seam::set_indexsafe_procflags::call()?;
    }

    // Phase 3: once again wait until no transaction can have the table open with
    // the index marked read-only for updates.
    pgstat_progress_update_param(PROGRESS_CREATEIDX_PHASE, PROGRESS_CREATEIDX_PHASE_WAIT_2);
    lmgr_seam::wait_for_lockers::call(heaplocktag, ShareLock_v(), true)?;

    // Take the "reference snapshot" validate_index() uses to filter candidate
    // tuples; also set ActiveSnapshot to it (functions in indexes may need one).
    let snapshot = snapmgr_seam::register_snapshot::call(
        snapmgr_seam::get_transaction_snapshot::call()?,
    )?;
    snapmgr_seam::push_active_snapshot::call(alloc::rc::Rc::new(snapshot.clone()))?;

    // Scan the index and the heap, insert any missing index entries (2nd pass).
    ::index::validate_index(mcx, table_id, index_relation_id, Some(snapshot.clone()))?;

    // Drop the reference snapshot. We must do this before waiting out other
    // snapshot holders, else we deadlock against other CREATE INDEX CONCURRENTLY
    // processes (which would see our snapshot as one they must wait for). But
    // first, save the snapshot's xmin to use as limitXmin for the wait.
    let limit_xmin = snapshot.xmin;

    snapmgr_seam::pop_active_snapshot::call()?;
    snapmgr_seam::unregister_snapshot::call(snapshot);

    // The snapshot subsystem could still hold registered snapshots that pin our
    // advertised xmin (serializable xact snapshot; the CatalogSnapshot). To
    // avoid deadlocks, commit + start yet another transaction and do our wait
    // before any snapshot has been taken in it.
    xact_seam::commit_transaction_command::call()?;
    xact_seam::start_transaction_command::call()?;

    if safe_index {
        proc_seam::set_indexsafe_procflags::call()?;
    }

    // The index is now valid in the sense that it contains all currently
    // interesting tuples; but it might not contain tuples deleted just before
    // the reference snap was taken, so wait out any transactions that might have
    // older snapshots.
    pgstat_progress_update_param(PROGRESS_CREATEIDX_PHASE, PROGRESS_CREATEIDX_PHASE_WAIT_3);
    WaitForOlderSnapshots(mcx, limit_xmin, true)?;

    // Updating pg_index might involve TOAST access, so ensure a valid snapshot.
    snapmgr_seam::push_active_snapshot::call(alloc::rc::Rc::new(
        snapmgr_seam::get_transaction_snapshot::call()?,
    ))?;

    // Index can now be marked valid -- update its pg_index entry.
    ::index::index_set_state_flags(
        mcx,
        index_relation_id,
        ::index_seams::IndexStateFlagsAction::SetValid,
    )?;

    snapmgr_seam::pop_active_snapshot::call()?;

    // The pg_index update causes backends (including us) to refresh the index's
    // relcache entry; also send a relcache inval on the parent table to force
    // replanning of cached plans, so existing sessions can use the new index.
    inval_seam::cache_invalidate_relcache::call(heaprelid.relId)?;

    // Release the session-level lock on the parent table.
    lmgr_seam::unlock_relation_id_for_session::call(heaprelid, ShareUpdateExclusiveLock_v())?;

    pgstat_progress_end_command();

    Ok(address)
}

/// `ShareUpdateExclusiveLock` (`storage/lockdefs.h`).
fn ShareUpdateExclusiveLock_v() -> ::types_storage::lock::LOCKMODE {
    4
}

/// `ShareLock` (`storage/lockdefs.h`).
fn ShareLock_v() -> ::types_storage::lock::LOCKMODE {
    5
}

/// `WaitForOlderSnapshots(limitXmin, progress)` (commands/indexcmds.c) — wait
/// for transactions that might have an older snapshot than `limitXmin`, used at
/// the end of a concurrent index build. Obtain the VXIDs of such transactions
/// (excluding autovacuum / VACUUM / safe-IC backends, and other DBs) and wait
/// for each individually, rechecking the live set each iteration so a backend
/// that goes idle-in-transaction with xmin 0 stops being waited on.
///
/// In single-backend mode `GetCurrentVirtualXIDs` reports no other backends, so
/// this returns immediately.
pub fn WaitForOlderSnapshots<'mcx>(
    mcx: Mcx<'mcx>,
    limit_xmin: ::types_core::TransactionId,
    progress: bool,
) -> PgResult<()> {
    use ::types_storage::storage::{PROC_IN_SAFE_IC, PROC_IN_VACUUM, PROC_IS_AUTOVACUUM};

    let exclude_vacuum = (PROC_IS_AUTOVACUUM | PROC_IN_VACUUM | PROC_IN_SAFE_IC) as i32;

    // old_snapshots = GetCurrentVirtualXIDs(limitXmin, true, false,
    //                     PROC_IS_AUTOVACUUM | PROC_IN_VACUUM | PROC_IN_SAFE_IC,
    //                     &n_old_snapshots);
    let mut old_snapshots = procarray_seam::get_current_virtual_xids::call(
        mcx,
        limit_xmin,
        true,
        false,
        exclude_vacuum,
    )?;
    let n_old_snapshots = old_snapshots.len();

    if progress {
        pgstat_progress_update_param(PROGRESS_WAITFOR_TOTAL, n_old_snapshots as i64);
    }

    for i in 0..n_old_snapshots {
        if !old_snapshots[i].is_valid() {
            continue; // found uninteresting in a previous cycle
        }

        if i > 0 {
            // See if anything's changed: re-read the live VXID set and forget
            // any old snapshot whose VXID is no longer present.
            let newer_snapshots = procarray_seam::get_current_virtual_xids::call(
                mcx,
                limit_xmin,
                true,
                false,
                exclude_vacuum,
            )?;
            for j in i..n_old_snapshots {
                if !old_snapshots[j].is_valid() {
                    continue;
                }
                let still_present = newer_snapshots.iter().any(|nv| {
                    nv.procNumber == old_snapshots[j].procNumber
                        && nv.localTransactionId == old_snapshots[j].localTransactionId
                });
                if !still_present {
                    old_snapshots[j] = ::types_core::xact::VirtualTransactionId::invalid();
                }
            }
        }

        if old_snapshots[i].is_valid() {
            if progress {
                // Publish who we're going to wait for (best-effort pid lookup).
                let pid = procarray_seam::proc_number_get_proc_pid::call(
                    old_snapshots[i].procNumber,
                );
                if pid != 0 {
                    pgstat_progress_update_param(PROGRESS_WAITFOR_CURRENT_PID, pid as i64);
                }
            }
            // VirtualXactLock(old_snapshots[i], true): wait for the xact to end.
            lock_seam::virtual_xact_lock::call(old_snapshots[i], true)?;
        }

        if progress {
            pgstat_progress_update_param(PROGRESS_WAITFOR_DONE, (i + 1) as i64);
        }
    }

    Ok(())
}

