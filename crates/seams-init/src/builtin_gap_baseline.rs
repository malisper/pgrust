//! KNOWN-GAP baseline for the fmgr built-in REGISTRY completeness guard.
//!
//! C makes `fmgr_builtins[]` complete by construction (`Gen_fmgrtab.pl` emits
//! every built-in from `pg_proc.dat`). The port assembles the equivalent set at
//! runtime from per-crate `register_builtins` calls, so completeness is only as
//! good as the set of ported+wired `adt/*` crates. Today that set is partial:
//! this file is the EXACT, checked-in inventory of every canonical built-in the
//! runtime registry is currently missing or has registered with diverging
//! metadata — the "visible gap" the completeness guard exists to surface.
//!
//! The `seams-init` test `builtin_registry_matches_canonical_or_baseline`
//! recomputes the live gap (after `init_all()`) and compares it to this baseline:
//!   * a gap NOT in this baseline = REGRESSION (a previously-registered built-in
//!     went missing, or a newly-canonical OID is unported) -> test FAILS LOUDLY,
//!     naming the OID + function. This is the guarantee the task asks for: a
//!     future unported/unwired builtin crate can never silently miss.
//!   * a baseline entry that is NO LONGER a gap = PROGRESS that wasn't recorded
//!     -> test fails, asking you to DELETE the now-fixed entry so the baseline
//!     ratchets monotonically toward empty (when empty, delete this file and
//!     flip the test to assert `missing_builtins()` is empty == full C parity).
//!
//! GENERATED — do not hand-edit rows. The normal path is INCREMENTAL: when the
//! completeness test reports a stale entry (now-fixed builtin), delete that one
//! line. A full regenerate (only needed after a PostgreSQL bump) is a temporary
//! test that runs `crate::init_all()` then formats `missing_builtins()` (sorted
//! by `foid`) as the `(oid, "prosrc", BuiltinGapKind::...)` rows below.
//!
//! The baseline is the maximum gap still accepted; the live gap is smaller and
//! ratchets toward empty as more `adt/*` builtin crates are registered.

