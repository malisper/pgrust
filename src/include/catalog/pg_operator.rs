fn operator_row_full(
    oid: u32,
    oprname: &str,
    oprkind: char,
    oprleft: u32,
    oprright: u32,
    oprresult: u32,
    oprcom: u32,
    oprnegate: u32,
    oprcode: u32,
    oprcanmerge: bool,
    oprcanhash: bool,
) -> PgOperatorRow {
    PgOperatorRow {
        oid,
        oprname: oprname.into(),
        oprnamespace: PG_CATALOG_NAMESPACE_OID,
        oprowner: BOOTSTRAP_SUPERUSER_OID,
        oprkind,
        oprcanmerge,
        oprcanhash,
        oprleft,
        oprright,
        oprresult,
        oprcom,
        oprnegate,
        oprcode,
        // :HACK: estimator functions are not cataloged yet in pgrust, so keep
        // `oprrest` / `oprjoin` as zero until that metadata exists.
        oprrest: 0,
        oprjoin: 0,
    }
}

