fn record_out_proc_row(
    oid: u32,
    proname: &str,
    proargtypes: &str,
    prosrc: &str,
    pronargs: i16,
    out_args: &[(&str, u32)],
) -> PgProcRow {
    let mut row =
        set_returning_proc_row(oid, proname, RECORD_TYPE_OID, proargtypes, prosrc, pronargs);
    row.proallargtypes = Some(
        parse_proc_argtype_oids(proargtypes)
            .unwrap_or_default()
            .into_iter()
            .chain(out_args.iter().map(|(_, oid)| *oid))
            .collect(),
    );
    row.proargmodes = Some(
        std::iter::repeat_n(b'i', pronargs as usize)
            .chain(std::iter::repeat_n(b'o', out_args.len()))
            .collect(),
    );
    row.proargnames = Some(
        std::iter::repeat_n(String::new(), pronargs as usize)
            .chain(out_args.iter().map(|(name, _)| (*name).to_string()))
            .collect(),
    );
    row
}

fn scalar_record_out_proc_row(
    oid: u32,
    proname: &str,
    proargtypes: &str,
    prosrc: &str,
    pronargs: i16,
    out_args: &[(&str, u32)],
) -> PgProcRow {
    let mut row = proc_row(
        oid,
        proname,
        RECORD_TYPE_OID,
        proargtypes,
        prosrc,
        pronargs,
        false,
        true,
        'f',
        'i',
    );
    row.proallargtypes = Some(
        parse_proc_argtype_oids(proargtypes)
            .unwrap_or_default()
            .into_iter()
            .chain(out_args.iter().map(|(_, oid)| *oid))
            .collect(),
    );
    row.proargmodes = Some(
        std::iter::repeat_n(b'i', pronargs as usize)
            .chain(std::iter::repeat_n(b'o', out_args.len()))
            .collect(),
    );
    row.proargnames = Some(
        std::iter::repeat_n(String::new(), pronargs as usize)
            .chain(out_args.iter().map(|(name, _)| (*name).to_string()))
            .collect(),
    );
    row
}

fn parse_proc_argtype_oids(argtypes: &str) -> Option<Vec<u32>> {
    if argtypes.trim().is_empty() {
        return Some(Vec::new());
    }
    argtypes
        .split_whitespace()
        .map(|part| part.parse::<u32>().ok())
        .collect()
}

fn oid_argtypes(arg_oids: &[u32]) -> String {
    arg_oids
        .iter()
        .map(|oid| oid.to_string())
        .collect::<Vec<_>>()
        .join(" ")
}

