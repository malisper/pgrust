fn proc_row(
    oid: u32,
    proname: &str,
    prorettype: u32,
    proargtypes: &str,
    prosrc: &str,
    pronargs: i16,
    proretset: bool,
    proisstrict: bool,
    prokind: char,
    provolatile: char,
) -> PgProcRow {
    proc_row_with_parallel(
        oid,
        proname,
        prorettype,
        proargtypes,
        prosrc,
        pronargs,
        proretset,
        proisstrict,
        prokind,
        provolatile,
        's',
    )
}

fn proc_row_with_parallel(
    oid: u32,
    proname: &str,
    prorettype: u32,
    proargtypes: &str,
    prosrc: &str,
    pronargs: i16,
    proretset: bool,
    proisstrict: bool,
    prokind: char,
    provolatile: char,
    proparallel: char,
) -> PgProcRow {
    PgProcRow {
        oid,
        proname: proname.into(),
        pronamespace: PG_CATALOG_NAMESPACE_OID,
        proowner: BOOTSTRAP_SUPERUSER_OID,
        prolang: PG_LANGUAGE_INTERNAL_OID,
        procost: 1.0,
        prorows: if proretset { 1000.0 } else { 0.0 },
        provariadic: 0,
        prosupport: 0,
        prokind,
        prosecdef: false,
        proleakproof: false,
        proisstrict,
        proretset,
        provolatile,
        proparallel,
        pronargs,
        pronargdefaults: 0,
        prorettype,
        proargtypes: proargtypes.into(),
        proallargtypes: None,
        proargmodes: None,
        proargnames: None,
        prosrc: prosrc.into(),
    }
}

